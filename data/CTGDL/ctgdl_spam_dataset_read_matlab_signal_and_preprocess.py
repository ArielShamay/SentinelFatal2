def plot_ctg_stacked(dp, trace_id='Trace', save_path='ctg_stacked.png'):
    fig = plt.figure(figsize=(12, 5), dpi=300)
    gs = gridspec.GridSpec(2, 1, height_ratios=[3, 1], hspace=0)

    t = np.arange(len(dp)) / 4/60  # time in minutes

    # --- FHR subplot (top) ---
    ax_fhr = fig.add_subplot(gs[0])
    ax_fhr.plot(t, dp['fhr'], color='black', linewidth=0.5)
    ax_fhr.set_ylim(30, 240)
    ax_fhr.set_ylabel('FHR (30–240)')
    ax_fhr.set_title(f' Record id:  {trace_id}, Duration {len(dp)//240} Minutes')
    ax_fhr.grid(True, which='both', linestyle='--', linewidth=0.3)
    ax_fhr.tick_params(axis='x', which='both', bottom=False, top=False, labelbottom=False)

    # Shaded zones for FHR
    ax_fhr.axhspan(30, 80, color='lightcoral', alpha=0.15)     # light red below 110
    ax_fhr.axhspan(80, 110, color='yellow', alpha=0.15)   # light yellow normal zone
    ax_fhr.axhspan(150, 180, color='yellow', alpha=0.15)   # light yellow above 150
    ax_fhr.axhspan(180, 240, color='lightcoral', alpha=0.15)    # light red above 180

    # --- TOCO subplot (bottom) ---
    ax_uc = fig.add_subplot(gs[1], sharex=ax_fhr)
    ax_uc.set_ylim(0, 100)
    ax_uc.set_ylabel('TOCO (0–100)')
    ax_uc.grid(True, which='both', linestyle='--', linewidth=0.3)
    ax_uc.set_xlabel('Time (minutes)')

    # Full-width light gray background for TOCO
    ax_uc.axhspan(0, 100, color='lightgray', alpha=0.15)

    # TOCO line on top of shading
    ax_uc.plot(t, dp['uc'], color='black', linewidth=0.5)

    plt.subplots_adjust(hspace=0)
    plt.savefig(save_path, dpi=300, bbox_inches='tight')
    plt.show()

